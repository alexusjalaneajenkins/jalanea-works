import type { NextConfig } from "next";

const nextConfig: NextConfig = {
  // Disable caching for HTML pages to prevent stale content
  async headers() {
    return [
      {
        // Apply to all routes
        source: '/:path*',
        headers: [
          {
            key: 'Cache-Control',
            value: 'no-store, must-revalidate',
          },
        ],
      },
    ];
  },
};

export default nextConfig;
